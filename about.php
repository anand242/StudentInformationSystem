           The Koneru Lakshmaiah Charities was established as a trust in the year 1980 with its official address at Museum road,
Governorpet, Vijayawada, Andhra Pradesh – 520 002 and started KL College of Engineering in the Academic year 1980-81. The trust was converted into a Society by the name Koneru Lakshmaiah Education Foundation in the year 1996. The KL College of Engineering has attained autonomous status in the year 2006 and in February 2009, the Koneru Lakshmaiah Education Foundation Society was recognized as Deemed to be University. In short Koneru Lakshmaiah Education Foundation is named as K L Deemed to be University.</

<h1>Location</h1>
<p>Vijayawada is located on the banks of river Krishna in the state of Andhra Pradesh and has been historically a cultural, political and educational center. The city is well connected by National Highway and Rail with Chennai (440 km), Hyderabad (275 km), Vizag (385 km) and is a central junction for trains running from North to South India. Daily flights operate from Hyderabad and Bangalore.

K L Deemed to be University is situated in a spacious 100-acre campus on the banks of Buckingham Canal of river Krishna, eight kilometers from Vijayawada city. Built within a rural setting of lush green fields, the institute is a virtual paradise of pristine nature and idyllic beauty. The campus has been aptly named "Green Fields" and the splendid avenue of trees and gardens bear testimony to the importance of ecology and environment. The campus ambience is most befitting for scholastic pursuits. The University has been situated on a built up area of around 15, 00, 000 S. Ft.</p> 

